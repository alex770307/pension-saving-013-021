/**
 * The CalculatorUtils class provides utility methods for performing
 * basic arithmetic calculations.
 */
public class CalculatorUtils {
    public static double averageValue(double first, double second) {
        return (first + second) / 2.0;
    }

    public static double averageValue(double first, double second, double third) {
        return (first + second + third) / 3.0;
    }
}
